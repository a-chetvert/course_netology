#pragma once

/**
 * @brief Типы гоночных транспортных средств
 * Определяет возможные категории транспорта для гонок.
 */
enum class RaceType
{
  gnd = 1,  ///< Наземный транспорт (автомобили, мотоциклы)
  air,      ///< Воздушный транспорт (самолеты, вертолеты)
  all       ///< Все типы транспорта (комбинированные гонки)
};
